    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // Check if it's locked
        if (isLocked) {
            JOptionPane.showMessageDialog(this, "Login is locked. Please wait.");
        return;
        }
        
        // Get userId and password
        String userId = userIdEnter.getText().trim();
        String password = new String(passwordEnter.getPassword());
        
        // Get userType
        String userFileType = AuthService.getUserFileType(userId);
        
        // Flag to track if login was succesful or not
        boolean credentialsValid = false;
        
        // Validate credentials
        if (userFileType == null) {
            failedAttempts++;
            JOptionPane.showMessageDialog(this, "Invalid user ID format.");
        }
        
        else if (!AuthService.foundID(userId, userFileType)){
            failedAttempts++;
            JOptionPane.showMessageDialog(this, "userID:"+ userId +"not found");
        }
        
        else if (AuthService.passwordIsCorrect(userId,password,userFileType)) {
            failedAttempts = 0;
            credentialsValid = true;
            JOptionPane.showMessageDialog(this, "Login Succesful!");
            
            // Open respective GUI based on user type
            switch (userFileType.toLowerCase()){
                
                case "student.txt" -> {
                    new StudentGUI().setVisible(true);
                }
                
                case "admin.txt" -> {
                    new AdminGUI().setVisible(true);
                }
                
                case "receptionist.txt" -> {
                    new ReceptGUI().setVisible(true);
                }
                
                case "tutor.txt" -> {
                    // new TutorGUI().setVisible(true);
                    TutorUI.showTutorMenu(userId, userFileType);
                }
                
                default -> {
                    JOptionPane.showMessageDialog(this, "Unkown user type.");
                    return;
                }
            }
            
            // Close login form
            this.dispose();
            
        } 
        else {
            failedAttempts++;
            JOptionPane.showMessageDialog(this, "Incorrect password. Attempt"+failedAttempts );
        }
            
        if (!credentialsValid && failedAttempts >= 3) {
            isLocked = true;
            loginButton.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Maximum failed attempt is 3 times. Please wait 5s and try again.");
            incorrectPassDisplay.setText("Login locked.");      
            incorrectPassDisplay.setForeground(Color.red);  
            
            // Start timer
            timer = new Timer(5000, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    isLocked = false;
                    loginButton.setEnabled(true);
                    failedAttempts = 0;
                    incorrectPassDisplay.setText("");
                    JOptionPane.showMessageDialog(LoginGUI.this, "Login unlocked. Please try again.");
                    timer.stop();   
                }
            });
            timer.setRepeats(false);
            timer.start();    
        } 
    }//GEN-LAST:event_loginButtonActionPerformed

